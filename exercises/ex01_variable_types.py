# This exercise is designed to understand how to define various types of variables and print them to the screen.

# Integer
a = 10

print("Integer:", a)
print(f"Integer printed in second way: {a}")


a = 20	

print("Integer after changing value::", a)
print()


# Float
b = 3.14

print("Float:", b)
print()

# String
c = "hello"

print("String:", c)
print(f"Element c[3]: {c[3]}")
print(f"c[1]+c[3]: {c[1]+c[3]}")
print()


# Boolean
d = False

print("Boolean:", d)

