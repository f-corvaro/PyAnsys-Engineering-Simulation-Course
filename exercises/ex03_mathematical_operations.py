# This program performs various mathematical operations on two numbers provided by the user.

# Prompt the user to enter two numbers
a = float(input("Enter a number, a: "))
b = float(input("Enter a number, b: "))

# Print the header for the operations section
print("OPERATIONS")

# Perform and print the result of addition
print("a + b =", a + b)
print()

# Perform and print the result of subtraction
print("a - b =", a - b)
print()

# Perform and print the result of multiplication
print("a * b =", a * b)
print()

# Perform and print the result of division
print("a / b =", a / b)
print()

# Perform and print the result of modulus (remainder of division)
print("a % b =", a % b)
print()

# Perform and print the result of exponentiation (a raised to the power of b)
print("a ** b =", a ** b)
print()

# Perform and print the result of integer division
print("a // b =", a // b)